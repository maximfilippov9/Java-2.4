package ru.netology.stats;

import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;

class StatsServiceTest {

    @Test
    void shouldSum() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long actual = service.sum(sales);
        long expected = 180;
        assertEquals(expected, actual);

    }
    @Test
    void shouldAverage() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long actual = service.average(sales);
        long expected = 15;
        assertEquals(expected, actual);

    }
    @Test
    void shouldMonthWithMaxSales() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long actual = service.monthWithMax(sales);
        long expected = 8;
        assertEquals(expected, actual);

    }
    @Test
    void shouldMonthWithMinSales() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long actual = service.monthWithMin(sales);
        long expected = 9;
        assertEquals(expected, actual);

    }
    @Test
    void shouldMonthCountMoreThanAverage() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long actual = service.monthCountMoreThanAverage(sales);
        long expected = 5;
        assertEquals(expected, actual);

    }
    @Test
    void shouldMonthCountLessThanAverage() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long actual = service.monthCountLessThanAverage(sales);
        long expected = 5;
        assertEquals(expected, actual);

    }
}